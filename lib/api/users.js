mod.configure = function(_options, callback)
{
    if (this.table) {

        for (const col of Object.values(this.schema)) {
            if (col.length === 1) col.length = this.maxLength;
            if (col.check?.max === 1) col.check.max = this.maxLength;
        }
        this.tables = { [this.table]: this.schema };
    }

    callback();
}

/**
 * Returns a user record by login or id
 * @param {object|string} query - user id or login or { id, login }
 * @param {object} [options]
 * @param {function(err:object, user:object)} callback returning found record
 * @memberof module:api/users
 * @method get
 */
mod.get = function(query, options, callback)
{
    if (typeof options === "function") callback = options, options = null;

    if (typeof query === "string") {
        query = { [lib.isUuid(query) ? "id" : "login"]: query };
    }

    if (query?.login) {
        db.get(mod.table, { login: query.login }, callback);
    } else

    if (query?.id) {
        const opts = {
            sort: "id",
            cacheKeyName: "id",
            count: 1,
            first: 1,
        };
        db.select(mod.table, { id: query.id }, opts, callback);
    } else {
        callback();
    }
}

