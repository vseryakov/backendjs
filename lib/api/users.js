/**
 * Verify current cookie session and set the user in the context on success, this can be used standalone or as middleware
 * @param {RequestContext} context
 * @param {function(err:object, user:object)} callback
 * @memberof module:api/users
 * @method verifySession
 */
mod.verifySession = function(context, callback)
{
    const session = api.session.parse(context);
    if (!session) {
        logger.debug("verifySession:", mod.name, "nosession:", context);
        return callback({ status: 401, message: mod.errInvalidSession });
    }

    mod.get(session.id, (err, user) => {
        if (err) return callback(err);

        if (!user) {
            logger.debug("verifySession:", mod.name, "expired:", context);
            return callback({ status: 401, message: mod.errInvalidSession });
        }

        if (user.expire_time > 0 && user.expire_time < Date.now()) {
            logger.debug("verifySession:", mod.name, "expired:", context, user.id, user.name, user.expire_time);
            return callback({ status: 401, message: mod.errInvalidSession });
        }

        if (!lib.includes(user.sessions, session.exp) || !api.session.verify(context, user.secret)) {
            logger.debug("verifySession:", mod.name, "invalid:", context, user.id, user.name, user.sessions);
            return callback({ status: 401, message: mod.errInvalidSession });
        }

        context.user = user;

        callback(err, user);
    });
}

/**
 * Verify API token from Authorization header and sets user in the context on success, this can be used standalone or as middleware.
 *
 * It must be in format: `Authorization: Basic base64(token)` or `Authorization: Bearer token`,
 *
 * An API token is created for corresponding columns in the user table, see {@link module:api/users.prepareToken}:
 * - `id`: generated UUIDv4 by {@link module:lib.uuid} as ussual or can be an existing user id to link multiple tokens for a user
 * - `login`: generated UUIDv4 by {@link module:lib.uuid}, unique primary key
 * - `secret`: generated UUIDv4 by {@link module:lib.uuid}
 * - __send the token as `${login}${secret}` back to user via secure method, like TLS....__
 * - hash the secret with SHA256 and store in the DB
 *
 * Verification is done the following way:
 * - get a user record from DB by `token.substr(0, 32)` if there is a prefix skip it,
 * the convention about prefix it must be separated by underscore from the token like `api_........`
 * - check the `user.expire_time` column, must be empty or in the future
 * - compare the stored user secret by comparing `SHA256(token.substr(32))` with {@link module:lib.timingSafeEqual}
 *
 * @param {RequestContext} context
 * @param {function(err:object, user:object)} callback
 * @memberof module:api/users
 * @method verifyToken
 */
mod.verifyToken = function(context, callback)
{
    const auth = context.auth;
    if (!auth.token) {
        logger.debug("verifyToken:", mod.name, "notoken:", context);
        return callback({ status: 401, message: mod.errInvalidSession });
    }

    // Skip optional prefix
    const pos = auth.token.indexOf("_") + 33;

    mod.get({ login: auth.token.substr(0, pos) }, (err, user) => {
        if (err) return callback(err);

        if (!user) {
            logger.debug("verifyToken:", mod.name, "notfound:", context);
            return callback({ status: 401, message: mod.errInvalidSession });
        }

        if (user.expire_time > 0 && user.expire_time < Date.now()) {
            logger.debug("verifyToken:", mod.name, "expired:", context, user.id, user.name, user.expire_time);
            return callback({ status: 401, message: mod.errInvalidSession });
        }

        const secret = lib.hash(auth.token.substr(pos));

        if (!lib.timingSafeEqual(user.secret, secret)) {
            logger.debug("verifyToken:", mod.name, "invalid:", context, user.id, user.name);
            return callback({ status: 401, message: mod.errInvalidSession });
        }

        context.user = user;

        callback(err, user);
    });
}

/**
 * Creates a cookie session and store .exp in the `user.sessions` property, cleanup expired sessions
 * @param {RequestContext} context
 * @param {object} user - validated user record
 * @memberof module:api/users
 * @method prepareSession
 */
mod.prepareSession = function(context, user)
{
    const session = api.session.create(context, user?.id, user?.secret);
    if (!session) return;

    // This is not atomic on purpose, not going to support simultaneous logins from multiple places at once
    user.sessions = lib.split(user.sessions, { datatype: "int" }).
                        filter(x => x > Date.now());

    user.sessions.push(session.exp);
}

/**
 * Prepare an API token before saving in the database, generates UUIDv4 in place login/secret properties.
 * @param {object} user - user record
 * @param {object} [options]
 * @param {string} [options.prefix] - prefix for the login part
 * @returns {string} token - formatted as `login+secret`
 * @memberof module:api/users
 * @method prepareToken
 * @example
 * api.users.prepareToken(query, { prefix:"test_" })
 * 'test_9f73e6cbcbe2440bb3e304ce1316507b1d1cfdedaf7841a6883c9bbf6cf02593'
 */
mod.prepareToken = function(user, options)
{
    user.login ??= lib.uuid(options?.prefix);

    const secret = lib.uuid();
    user.secret = lib.hash(secret);

    return `${user.login}${secret}`;
}

/**
 * Prepare a MFA code to be sent and saved in `mfa_code` property
 * @param {object} user - user record
 * @param {object} [options]
 * @param {string} [options.method=email]
 * @returns {number} code - generated random code
 * @memberof module:api/users
 * @method prepareMFA
 * @example
 * const { sendmail, api } = required("backendjs");
 *
 * const user = await api.users.get('user@email');
 * const code = api.users.prepareMFA(user);
 * await api.users.aupdate({ login: user.login, mfa_code: user.mfa_code });
 *
 * sendmail.send({ to: user.login, subject: "Verification code", text: `Your verification code ${code}`, lib.log)
 */
mod.prepareMFA = function(user, options)
{
    const code = lib.randomInt(100000, 999999);
    user.mfa_code = `${code},${Date.now() + mod.mfaAge},${options?.method || "email"}`;
    return code;
}

/**
 * Prepare a new TOTP secret to be saved in the user record
 * @param {object} user - user record
 * @returns {object} - same user record with updated topt_secret property
 * @memberof module:api/users
 * @method prepareTOTP
 * @example
 * const user = await api.users.get('user@email')
 * const code = api.users.prepareTOTP(user);
 * await api.users.aupdate({ login: user.login, totp_secret: user.totp_secret });
 */
mod.prepareTOTP = function(user, _options)
{
    user.totp_secret = lib.toBase32(Buffer.from(lib.random().slice(0, 16)));
    return user;
}

/**
 * Login with just the secret, set the user in the context, creates a cookie session and
 * store .exp in the `sessions` column, cleanup expired sessions.
 *
 * Verification code will be enforced if a user has TOPT secret or mfa code saved.
 *
 * In case MFA is wrong or missing the error object will contain a property `code: "MFA"` to signal the UI
 * to deal with it by asking for code or using a separate endpoint to send it.
 * The user record is returned in the callback because it was verified already.
 *
 * @param {RequestContext} context
 * @param {object} context.body - login credentials
 * @param {string} context.body.login - username or email
 * @param {string} context.body.secret - clear text password
 * @param {string} [context.body.code] - optional MFA/TOTP code if enabled
 * @param {function(err:object, user:object)} callback
 * @memberof module:api/users
 * @method login
 * @example
 *
 * api.app.post("/login", (context) => {
 *
 *    api.users.login(context, async (err, user) => {
 *
 *        if (err?.code === "MFA" && user.mfa_code) {
 *            const code = api.users.prepareMFA(user);
 *            await api.users.aupdate({ login: user.login, mfa_code: user.mfa_code });
 *            await sendmail.asend({ to: user.login, subject: "Verification code", text: `Your verification code ${code}`);
 *            err.message += "\nPlease provide the code that was sent to your email";
 *        } else {
 *            err.message += "\nPlease provide code from your auth app";
 *        }
 *        context.reply(err, api.users.cleanup(user));
 *    })
 * })
 */
mod.login = function(context, callback)
{
    const { err, data } = api.validate(context, {
        login: { required: 1 },
        secret: { required: 1 },
        code: {},
    });
    if (err) return lib.tryCall(callback, err);

    mod.checkSecret(data, (err, user) => {
        if (err) return lib.tryCall(callback, err);

        const now = Date.now();

        let totp, mfa;

        if (user.totp_secret) {
            totp = data.code === lib.totp(user.totp_secret);
        }

        if (user.mfa_code) {
            const [code, expires] = lib.split(user.mfa_code);
            mfa = data.code === code && lib.toNumber(expires) > now;
        }

        if (totp === true || mfa === true) {
            logger.debug("login:", mod.name, "mfa:", context, user.id, user.name, totp, mfa);
        } else

        if (totp === false || mfa === false) {
            logger.debug("login:", mod.name, "nomfa:", context, user.id, user.name, totp, mfa);
            return lib.tryCall(callback, { status: 401, message: mod.errInvalidCode, code: "MFA" }, user);
        }

        mod.prepareSession(context, user);

        mod.update({ login: user.login, login_time: now, access_time: now, sessions: user.sessions }, callback);
    });
}

